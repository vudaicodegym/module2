package bt_Access_modifier;

import java.awt.geom.Area;

public class Circle {
    private double radius = 1.0;
    private String color = "blue";

    public Circle(double radius) {
        this.radius = radius;
    }

    public Circle() {
    }

    public double getRadius() {
        return radius;
    }
    public String getArea() {
        return color = "black";
    }
}
